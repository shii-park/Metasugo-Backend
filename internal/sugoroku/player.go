func (p *Player) moveNextTile() {
	if p.position.next != nil {
		p.position = p.position.next
	}
}

// TODO: エラー文の追加
func (p *Player) movePrevTile() {
	if p.position.prev != nil {
		p.position = p.position.prev
	}
}

func (p *Player) MoveByDiceRoll(steps int) {
	for i := 0; i < steps; i++ {
		p.moveNextTile()
	}
}
